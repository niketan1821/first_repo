let value1 = "2";
let value2 = "4";
let opp = "*";
switch (opp)
 {
    case "+":
        console.log(value1 + value2);
        break;
    case "-":
        console.log(value1 - value2);
        break;
    case "*":
        console.log(value1 * value2); 
    case "/":
        console.log(value1 / value2);
        break;
    case "%":
        console.log(value1 % value2);
        break;
        }
